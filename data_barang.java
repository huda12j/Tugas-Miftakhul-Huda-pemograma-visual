    private void tabel_barangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_barangMouseClicked
    int bar = tabel_barang.getSelectedRow(); // baris yang diklik
    if (bar != -1) { // Memastikan baris yang dipilih valid
        String a = tabmode.getValueAt(bar, 0).toString(); // Kode Barang
        String b = tabmode.getValueAt(bar, 1).toString(); // Nama Barang
        String c = tabmode.getValueAt(bar, 2).toString(); // Jenis Barang
        String d = tabmode.getValueAt(bar, 3).toString(); // Harga Beli
        String e = tabmode.getValueAt(bar, 4).toString(); // Harga Jual

    txt_kd.setText(a);
    txt_nama.setText(b);
    txt_jenis.setText(c);
    txt_beli.setText(d);
    txt_jual.setText(e);
    } else {
        JOptionPane.showMessageDialog(null, "Pilih baris terlebih dahulu!");
}     // TODO add your handling code here:
    }//GEN-LAST:event_tabel_barangMouseClicked

