       protected void datatable(){
        Object[] Baris={"ID Pelanggan","Nama Barang","Jenis Barang","Harga Beli","Harga Jual"};//nama kolom tabel di form
        tabmode = new DefaultTableModel(null,Baris);
        
        try{
            String sql = "SELECT * FROM barang_penjualan ORDER BY id ASC";
            Statement stat= conn.createStatement();
            ResultSet hasil=stat.executeQuery(sql);
            while(hasil.next()){
                tabmode.addRow(new Object[]{
                hasil.getString(1),
                hasil.getString(2),
                hasil.getString(3),
                hasil.getString(4),
                hasil.getString(5),
            });
            }
            tabel_barang.setModel(tabmode);
       }catch (Exception e){
    JOptionPane.showMessageDialog(null,"data gagal dipanggil"+ e);
        } 
    }   

